#!/usr/bin/env node
// Regenerates the Portfolio grid in index.html and the ajax/portfolio-ajax-project-*.html
// detail popups from data/projects.json. Run this after editing projects.json by hand,
// or let .github/workflows/build.yml run it automatically after the CMS commits.

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const DATA_PATH = path.join(ROOT, 'data', 'projects.json');
const INDEX_PATH = path.join(ROOT, 'index.html');
const AJAX_DIR = path.join(ROOT, 'ajax');

const GRID_START = '<!-- PORTFOLIO-GRID:START -- generated by scripts/build-portfolio.js from data/projects.json, do not edit by hand -->';
const GRID_END = '<!-- PORTFOLIO-GRID:END -->';

function escapeHtml(value) {
  return String(value == null ? '' : value)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

function buildCard(project, ajaxFileName) {
  const categories = (project.categories || []).join(' ');
  return `            <div class="col-sm-6 col-lg-4 ${escapeHtml(categories)}">
              <div class="portfolio-box rounded my-3">
                <div class="portfolio-img rounded"> <img class="img-fluid d-block" src="${escapeHtml(project.coverImage)}" alt="">
                  <div class="portfolio-overlay"> <a class="popup-ajax stretched-link" href="ajax/${ajaxFileName}"></a>
                    <div class="portfolio-overlay-details">
                      <h5 class="text-white font-weight-400">${escapeHtml(project.title)}</h5>
                      <span class="text-light">${escapeHtml(project.subtitle)}</span> </div>
                  </div>
                </div>
              </div>
            </div>`;
}

function buildAjaxFragment(project) {
  const images = project.images && project.images.length ? project.images : [project.coverImage];
  const slides = images
    .map((src) => `        <div class="item"> <img class="img-fluid" alt="" src="${escapeHtml(src)}"> </div>`)
    .join('\n');

  return `<div class="container ajax-container">
  <h2 class="text-6 font-weight-600 text-center mb-4">${escapeHtml(project.detailTitle || project.title)}</h2>
  <div class="row">
    <div class="col-sm-7 mb-4 mb-md-0">
      <div class="owl-carousel owl-theme single-slideshow" data-autoplay="true" data-loop="true" data-nav="true" data-items="1">
${slides}
      </div>
    </div>
    <div class="col-sm-5">
      <h4 class="text-4 font-weight-600">Project Info:</h4>
      <p>${escapeHtml(project.description)}</p>
      <h4 class="text-4 font-weight-600 mt-4">Project Details:</h4>
      <ul class="list-style-2">
        <li class=""><span class="text-dark font-weight-600 mr-2">Category:</span> ${escapeHtml(project.categoryLabel)}</li>
        <li class=""><span class="text-dark font-weight-600 mr-2">Date:</span> ${escapeHtml(project.date)}</li>
        <li class=""><span class="text-dark font-weight-600 mr-2">URL:</span><a href="${escapeHtml(project.url)}" target="_blank">${escapeHtml(project.urlLabel)}</a></li>
      </ul>
      <div class="row no-gutters align-items-center">
        <div class="col-auto text-dark font-weight-600">Share: </div>
        <div class="col-auto">
          <ul class="social-icons">
            <li class="social-icons-facebook"><a data-toggle="tooltip" href="http://www.facebook.com/" target="_blank" title="" data-original-title="Facebook"><i class="fab fa-facebook-f"></i></a></li>
            <li class="social-icons-twitter"><a data-toggle="tooltip" href="http://www.twitter.com/" target="_blank" title="" data-original-title="Twitter"><i class="fab fa-twitter"></i></a></li>
            <li class="social-icons-google"><a data-toggle="tooltip" href="http://www.google.com/" target="_blank" title="" data-original-title="Google"><i class="fab fa-google"></i></a></li>
            <li class="social-icons-instagram"><a data-toggle="tooltip" href="http://www.instagram.com/" target="_blank" title="" data-original-title="Instagram"><i class="fab fa-instagram"></i></a></li>
            <li class="social-icons-email"><a data-toggle="tooltip" href="http://www.instagram.com/" target="_blank" title="" data-original-title="Email"><i class="fas fa-envelope"></i></a></li>
          </ul>
        </div>
      </div>
    </div>
  </div>
</div>
`;
}

function main() {
  const { projects } = JSON.parse(fs.readFileSync(DATA_PATH, 'utf8'));
  if (!Array.isArray(projects)) {
    throw new Error('data/projects.json must contain a "projects" array');
  }

  // Wipe and regenerate the numbered ajax fragments so stale files never linger.
  fs.readdirSync(AJAX_DIR)
    .filter((name) => /^portfolio-ajax-project-\d+\.html$/.test(name))
    .forEach((name) => fs.unlinkSync(path.join(AJAX_DIR, name)));

  const cards = [];
  projects.forEach((project, index) => {
    const ajaxFileName = `portfolio-ajax-project-${index + 1}.html`;
    fs.writeFileSync(path.join(AJAX_DIR, ajaxFileName), buildAjaxFragment(project));
    cards.push(buildCard(project, ajaxFileName));
  });

  const indexHtml = fs.readFileSync(INDEX_PATH, 'utf8');
  const startIdx = indexHtml.indexOf(GRID_START);
  const endIdx = indexHtml.indexOf(GRID_END);
  if (startIdx === -1 || endIdx === -1) {
    throw new Error('Could not find PORTFOLIO-GRID markers in index.html');
  }

  const before = indexHtml.slice(0, startIdx + GRID_START.length);
  const after = indexHtml.slice(endIdx);
  const updated = `${before}\n${cards.join('\n')}\n            ${after}`;
  fs.writeFileSync(INDEX_PATH, updated);

  console.log(`Rebuilt portfolio: ${projects.length} projects, ${projects.length} ajax fragments.`);
}

main();
